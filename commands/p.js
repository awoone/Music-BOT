const { Util } = require("discord.js");
const ytdl = require("ytdl-core");
const YouTube = require("simple-youtube-api");

exports.run = async (client, message, args) => {
    const { channel } = message.member.voice;
    if (!channel)
      return message.channel.send(
        'VocÃª precisa estar em um canal de mÃºsica primeiro!'
      );
    const permissions = channel.permissionsFor(message.client.user);
    if (!permissions.has("CONNECT"))
      return message.channel.send(
        'Eu nÃ£o posso entrar nesse canal ai. Da uma olhada se tenho permissÃ£o!'
      );
    if (!permissions.has("SPEAK"))
      return message.channel.send(
        'Eu nÃ£o posso falar nesse canal ai. Da uma olhada se tenho permissÃ£o!'
      );
    const youtube = new YouTube(client.config.api);
    var searchString = args.join(" ");
    if (!searchString)
      return message.channel.send("VocÃª precisa pesquisar por alguma mÃºsica.");
    const serverQueue = message.client.queue.get(message.guild.id);
    var videos = await youtube.searchVideos(searchString).catch(console.log);
    var songInfo = await videos[0].fetch().catch(console.log);

    const song = {
      id: songInfo.video_id,
      title: Util.escapeMarkdown(songInfo.title),
      url: songInfo.url,
    };

    if (serverQueue) {
      serverQueue.songs.push(song);
      console.log(serverQueue.songs);
      const embed = {
				"title": 'Proerd â„¢ - Music',
				"description": 'âœ… **' + song.title + '** foi adicionada na fila!',
				"color": "YELLOW",
			  };
			  return message.channel.send({embed});
    }

    const queueConstruct = {
      textChannel: message.channel,
      voiceChannel: channel,
      connection: null,
      songs: [],
      volume: 2,
      playing: true,
    };
    message.client.queue.set(message.guild.id, queueConstruct);
    queueConstruct.songs.push(song);

    const play = async (song) => {
      const queue = message.client.queue.get(message.guild.id);
      if (!song) {
        queue.voiceChannel.leave();
        message.client.queue.delete(message.guild.id);
        return;
      }

      const dispatcher = queue.connection
        .play(ytdl(song.url))
        .on("finish", () => {
          queue.songs.shift();
          play(queue.songs[0]);
        })
        .on("error", (error) => console.error(error));
      dispatcher.setVolumeLogarithmic(queue.volume / 5);
      const embed = {
				"title": 'Proerd â„¢ - Music',
				"description": 'ðŸŽ¶ Tocando: **' + song.title + '**',
				"color": "YELLOW",
			  };
			  queue.textChannel.send({embed});
    };

    try {
      const connection = await channel.join();
      queueConstruct.connection = connection;
      play(queueConstruct.songs[0]);
    } catch (error) {
      console.error(`Eu nÃ£o posso entrar no canal de voz: ${error}`);
      message.client.queue.delete(message.guild.id);
      await channel.leave();
      return message.channel.send(
        `Eu nÃ£o posso entrar no canal de voz: ${error}`
      );
    }
};
